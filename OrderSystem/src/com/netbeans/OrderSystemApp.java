    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
        //获取productNameTx文本框内容
        String productName = productNameTx.getText();
        //获取商品信息
        List<Goods> list = DbManager.getAllGoods(productName);	
        //获取表model
        DefaultTableModel TableM = (DefaultTableModel) Menu.getModel();
        TableM.setRowCount(0);
        for(Goods item : list){
            //渲染到表的每行数据
                TableM.addRow(new Object[] { item.getId(), item.getName(), item.getQuantity(), item.getPrice()});
                Menu.updateUI();     
        }
    }//GEN-LAST:event_ConfirmActionPerformed

