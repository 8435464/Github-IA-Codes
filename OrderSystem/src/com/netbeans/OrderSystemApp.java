    private void jAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddActionPerformed
        //拿到数量文本框的值
        String numStr = jQty.getText();
         //数量为空则直接跳出
         if(numStr == null || numStr.trim().equals(""))return;
         int num = Integer.parseInt(numStr);
        int row = Menu.getSelectedRow();//获取选中的行号
        DefaultTableModel model = (DefaultTableModel) Menu.getModel();//获取defaulttablemodel
        
        //拿到左侧表内的数据
        Object id = model.getValueAt(row,0);
        Object name = model.getValueAt(row,1);
        Object price = model.getValueAt(row,3);
        float total = num * (float)price;
        //放到右侧表格内
        DefaultTableModel TableM = (DefaultTableModel) shoppingCartTbl.getModel();
        TableM.addRow(new Object[] {id,name,num,price,total});
        shoppingCartTbl.updateUI();
    }//GEN-LAST:event_jAddActionPerformed
    
    private void Sum_upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Sum_upActionPerformed
        //拿到右侧表格数据
        DefaultTableModel TableM = (DefaultTableModel) shoppingCartTbl.getModel();
        int rowCount = TableM.getRowCount();
        float totalPrice = 0f;
        int totalNum = 0;
        
        //先清空待结算商品队列
        waitingList.clear();
        
        for(int i=0;i< rowCount; i++){
            //读取表格中价格一栏
            Object price = TableM.getValueAt(i,4);
            //读取表格中数量一栏
            Object num = TableM.getValueAt(i,2);
            //计算
            totalPrice += (float)price;
            totalNum += (int)num;
            
            //生成商品信息
            Goods item = new Goods();
            item.setId((int) TableM.getValueAt(i,0));
            item.setName((String) TableM.getValueAt(i,1));
            item.setPrice((float)price);
            item.setQuantity((int)num);
            //加入待结算商品队列中
            waitingList.add(item);
        }
        //将结果显示到两个文本框中
        jTotal.setText(totalNum+"");
        jNetPrice.setText(totalPrice +"");
        
    }//GEN-LAST:event_Sum_upActionPerformed

    private void PrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrintActionPerformed
       //右侧发票信息显示
        String chart = "                List of Goods/商品清单\n\n";
        for(Goods item : waitingList){
            String content = "Product name/商品名称：" + item.getName() + 
                    "    Purchased quantity/数量" + item.getQuantity() + 
                    "    Price/价格" + item.getPrice() + "\n";
            chart += content;
            
            //商品信息更新
            item.setSale(item.getQuantity());
            DbManager.update(item);
            
            //交易信息保存
            TransFlow transFlow = new TransFlow();
            transFlow.setProName(item.getName());
            //transFlow.setAmt(item.getPrice() * item.getQuantity());
            transFlow.setAmt(item.getPrice());
            transFlow.setBal(item.getQuantity());
            transFlow.setTransDate(new Date(System.currentTimeMillis()));
            DbManager.save(transFlow);
        }
        chart += "\n      Total/总计:" + jNetPrice.getText() + "Yuan/元\n";
        BillsArea.setText(chart);
        
    }//GEN-LAST:event_PrintActionPerformed

